// ─── 主入口 ───────────────────────────────────────────────────────────────

/**
 * 根据产品名和首个应用名，生成完整的 Solo Monorepo 文件清单
 * @param productName 产品名（原始输入）
 * @param appName     首个应用英文简称（原始输入）
 * @returns 文件清单（path 相对于 workDir）
 */
export function buildProductFileList(
  productName: string,
  appName: string,
): ProductFileEntry[] {
  const kebab = toKebabCase(productName);
  const pl = `${kebab}-pl`;
  const domain = `${kebab}-domain`;
  const app = toKebabCase(appName);

  return [
    // .xingjing/config.yaml（星静自身配置）
    {
      path: '.xingjing/config.yaml',
      content: [
        `name: ${productName}`,
        `version: "1.0.0"`,
        `created-at: ${new Date().toISOString()}`,
        `product-line: ${pl}`,
        `domain: ${domain}`,
        `apps:`,
        `  - ${app}`,
        '',
      ].join('\n'),
    },
    // §8.3 根目录
    ...buildMonorepoRoot(productName),
    // §7.1 平台层
    ...buildGovernanceStandards(),
    // §7.2 产品线层
    ...buildProductLine(pl, productName),
    // §7.3 领域层
    ...buildDomain(domain, productName),
    // §7.4 应用层
    ...buildApp(app, productName),
  ];
}

/**
 * 将文件清单转换为目录树结构（用于 UI 展示）
 */
export interface DirTreeNode {
  name: string;
  path: string;
  isDir: boolean;
  children: DirTreeNode[];
}

export function buildDirTree(files: ProductFileEntry[]): DirTreeNode[] {
  const root: DirTreeNode[] = [];

  for (const f of files) {
    const parts = f.path.split('/');
    let current = root;

    for (let i = 0; i < parts.length; i++) {
      const name = parts[i];
      const isDir = i < parts.length - 1;
      const path = parts.slice(0, i + 1).join('/');

      let node = current.find((n) => n.name === name);
      if (!node) {
        node = { name, path, isDir, children: [] };
        current.push(node);
      }
      if (isDir) {
        current = node.children;
      }
    }
  }

  return root;
}
