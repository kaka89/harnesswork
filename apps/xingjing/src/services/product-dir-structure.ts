// ─── 内容生成辅助函数 ─────────────────────────────────────────────────────────

const EMPTY = '';
const INDEX_YAML = 'items: []\n';

function mdFrontmatter(meta: Record<string, string>): string {
  const lines = Object.entries(meta).map(([k, v]) => `${k}: ${v}`);
  return `---\n${lines.join('\n')}\n---\n\n`;
}

function overviewMd(title: string, description: string): string {
  return mdFrontmatter({ title, lifecycle: 'living', 'created-at': new Date().toISOString().slice(0, 10) })
    + `# ${title}\n\n${description}\n`;
}

function knowledgeMd(title: string): string {
  return mdFrontmatter({ title, lifecycle: 'living', 'created-at': new Date().toISOString().slice(0, 10) })
    + `# ${title}\n`;
}

function roadmapMd(scope: string): string {
  return mdFrontmatter({ title: `${scope} Roadmap`, lifecycle: 'living', 'created-at': new Date().toISOString().slice(0, 10) })
    + `# ${scope} Roadmap\n\n## Milestones\n\n_TODO_\n`;
}

function emptyYaml(): string {
  return '# Auto-generated by xingjing\n';
}

function ownersFile(name: string): string {
  return `# ${name}\n# Add owner GitHub handles below\n`;
}

function readmeFile(title: string, description: string): string {
  return `# ${title}\n\n${description}\n`;
}

